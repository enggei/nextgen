package nextgen.react.st;


public class ClassComponentEvents {

	private java.lang.Object methodName;

	public ClassComponentEvents() { }

	public ClassComponentEvents(java.lang.Object methodName) { 
		this.methodName = methodName;
	}

	public java.lang.Object getMethodName() { 
		return methodName;
	}

	public ClassComponentEvents setMethodName(java.lang.Object methodName) { 
		this.methodName = methodName;
		return this;
	}
}