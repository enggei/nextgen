package nextgen.react.st;


public class AppComponents {

	private java.lang.Object path;
	private java.lang.Object name;

	public AppComponents() { }

	public AppComponents(java.lang.Object path, java.lang.Object name) { 
		this.path = path;
		this.name = name;
	}

	public java.lang.Object getPath() { 
		return path;
	}

	public AppComponents setPath(java.lang.Object path) { 
		this.path = path;
		return this;
	}

	public java.lang.Object getName() { 
		return name;
	}

	public AppComponents setName(java.lang.Object name) { 
		this.name = name;
		return this;
	}
}