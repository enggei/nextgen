package nextgen.react.st;


public class StyleProperties {

	private java.lang.Object key;
	private java.lang.Object value;

	public StyleProperties() { }

	public StyleProperties(java.lang.Object key, java.lang.Object value) { 
		this.key = key;
		this.value = value;
	}

	public java.lang.Object getKey() { 
		return key;
	}

	public StyleProperties setKey(java.lang.Object key) { 
		this.key = key;
		return this;
	}

	public java.lang.Object getValue() { 
		return value;
	}

	public StyleProperties setValue(java.lang.Object value) { 
		this.value = value;
		return this;
	}
}